    public function findUserArticles($current_page, $num_result)
    {
        $qb = $this->createQueryBuilder('u');
        $qb
            ->select('u.username, u.isActive, a.title, a.body, a.id')
            ->innerJoin('App:Article', 'a', 'WITH', 'a.user = u.id')
            ->setFirstResult($current_page * $num_result)
            ->setMaxResults($num_result);
        //var_dump($qb->getDQL());die;

        return $qb->getQuery()->getArrayResult();
    }

